//在每个模块目录中添加一个route.php文件，这里加载此文件
$routes = glob(APP_PATH . '/routes/' . '*.php');
foreach ($routes as $route) {
    if (file_exists($route)) {
        include_once $route;
    }
}

return [
    '__pattern__' => [
        'name' => '\w+',
    ],
    '[hello]'     => [
